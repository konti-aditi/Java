class nfloat 
{
	public static void main(String[] args) 
	{
		float a = 127.98f;
		System.out.println(a);
		
		long b = (long)a;
		System.out.println(b);
		
		int c = (int)a;
		System.out.println(c);
		
		char d = (char)a;
		System.out.println(d);
		
		short e = (short)a;
		System.out.println(e);
		
		byte f = (byte)a;
		System.out.println(f);
	}
}
