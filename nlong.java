class nlong 
{
	public static void main(String[] args) 
	{
		long a = 1234684395l;
		System.out.println(a);
		
		int b = (int)a;
		System.out.println(b);
		
		char c = (char)a;
		System.out.println(c);
		
		short d = (short)a;
		System.out.println(d);
		
		byte e = (byte) a;
		System.out.println(e);
	}
}
